int CClassView::OnCreate(LPCREATESTRUCT lpCreateStruct)
{
	if (CDockablePane::OnCreate(lpCreateStruct) == -1)
		return -1;

	SetWindowLongPtr(GetSafeHwnd(), DWLP_MSGRESULT, CDRF_NOTIFYITEMDRAW);

	CRect rectDummy;
	rectDummy.SetRectEmpty();

	// Create views:
	const DWORD dwViewStyle = WS_CHILD | WS_VISIBLE | WS_BORDER | TVS_HASLINES | TVS_LINESATROOT | TVS_HASBUTTONS | WS_CLIPSIBLINGS | WS_CLIPCHILDREN | TVS_SHOWSELALWAYS;

	if (!m_wndClassView.Create(dwViewStyle, rectDummy, this, 902))
	{
		TRACE0("Failed to create Class View\n");
		return -1;      // fail to create
	}

	m_wndClassView.SetExtendedStyle(TVS_EX_DOUBLEBUFFER, TVS_EX_DOUBLEBUFFER);
	m_wndClassView.SetAdditionalTextCallback([](CustomTreeCtrl* tree, HTREEITEM item, CString& additionalText)
		{

			CString itemText = tree->GetItemText(item);
			if (itemText.Find(_T("~")) != -1)
			{
				additionalText = _T("Destructor");
			}
			else
			{
				HTREEITEM parent = tree->GetParentItem(item);
				if (parent)
				{
					CString parentItemText = tree->GetItemText(parent);
					if(parentItemText + _T("()") == itemText)
						additionalText = _T("Constructor");
				}
			}
		});

	m_search.Create(WS_VISIBLE | WS_CHILD | WS_BORDER, CRect(), this, 1001);
	m_search.SetFont(&afxGlobalData.fontRegular);
	m_search.SetCueBanner(_T("Search"));

	m_loc.Create(WS_VISIBLE | WS_CHILD | WS_BORDER, CRect(), this, 1002);
	m_loc.SetFont(&afxGlobalData.fontRegular);
	m_loc.SetWindowText(_T("12.218, 2.55, 121.71"));
	
	// m_loc.EnablePreview(-100.0f, 100.0f, -100.0f, 100.0f);

	// Load images:
	m_wndToolBar.Create(this, AFX_DEFAULT_TOOLBAR_STYLE, IDR_SORT);
	m_wndToolBar.LoadToolBar(IDR_SORT, 0, 0, TRUE /* Is locked */);

	OnChangeVisualStyle();

	m_wndToolBar.SetPaneStyle(m_wndToolBar.GetPaneStyle() | CBRS_TOOLTIPS | CBRS_FLYBY);
	m_wndToolBar.SetPaneStyle(m_wndToolBar.GetPaneStyle() & ~(CBRS_GRIPPER | CBRS_SIZE_DYNAMIC | CBRS_BORDER_TOP | CBRS_BORDER_BOTTOM | CBRS_BORDER_LEFT | CBRS_BORDER_RIGHT));

	m_wndToolBar.SetOwner(this);

	// All commands will be routed via this control , not via the parent frame:
	m_wndToolBar.SetRouteCommandsViaFrame(FALSE);

	CMenu menuSort;
	menuSort.LoadMenu(IDR_POPUP_SORT);

	m_wndToolBar.ReplaceButton(ID_SORT_MENU, CClassViewMenuButton(menuSort.GetSubMenu(0)->GetSafeHmenu()));

	CClassViewMenuButton* pButton =  DYNAMIC_DOWNCAST(CClassViewMenuButton, m_wndToolBar.GetButton(0));

	if (pButton != nullptr)
	{
		pButton->m_bText = FALSE;
		pButton->m_bImage = TRUE;
		pButton->SetImage(GetCmdMgr()->GetCmdImage(m_nCurrSort));
		pButton->SetMessageWnd(this);
	}

	// Fill in some static tree view data (dummy code, nothing magic here)
	FillClassView();

	return 0;
}

