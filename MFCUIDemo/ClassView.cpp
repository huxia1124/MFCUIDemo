void CClassView::OnContextMenu(CWnd* pWnd, CPoint point)
{
	CTreeCtrl* pWndTree = (CTreeCtrl*)&m_wndClassView;
	ASSERT_VALID(pWndTree);

	if (pWnd != pWndTree)
	{
		CDockablePane::OnContextMenu(pWnd, point);
		return;
	}

	if (point != CPoint(-1, -1))
	{
		// Select clicked item:
		CPoint ptTree = point;
		pWndTree->ScreenToClient(&ptTree);

		UINT flags = 0;
		HTREEITEM hTreeItem = pWndTree->HitTest(ptTree, &flags);
		if (hTreeItem != nullptr)
		{
			pWndTree->SelectItem(hTreeItem);
		}
	}

	pWndTree->SetFocus();
	CMenu menu;
	menu.LoadMenu(IDR_POPUP_SORT);

	CMenu* pSumMenu = menu.GetSubMenu(0);

	if (AfxGetMainWnd()->IsKindOf(RUNTIME_CLASS(CMDIFrameWndEx)))
	{
		CMFCPopupMenu* pPopupMenu = new CMFCPopupMenu;

		if (!pPopupMenu->Create(this, point.x, point.y, (HMENU)pSumMenu->m_hMenu, FALSE, TRUE))
			return;

		((CMDIFrameWndEx*)AfxGetMainWnd())->OnShowPopupMenu(pPopupMenu);
		UpdateDialogControls(this, FALSE);
	}
}

void CClassView::AdjustLayout()
{
	if (GetSafeHwnd() == nullptr)
	{
		return;
	}

	CRect rectClient;
	GetClientRect(rectClient);

	int cyTlb = m_wndToolBar.CalcFixedLayout(FALSE, TRUE).cy;

	m_wndToolBar.SetWindowPos(nullptr, rectClient.left, rectClient.top, rectClient.Width(), cyTlb, SWP_NOACTIVATE | SWP_NOZORDER);

	const int searchBarHeight = static_cast<int>(afxGlobalData.GetRibbonImageScale() * 20.0);
	m_search.SetWindowPos(nullptr, rectClient.left + 1, rectClient.top + cyTlb + 1, rectClient.Width() - 2, searchBarHeight, SWP_NOACTIVATE | SWP_NOZORDER);
	
	const int bottomAreaHeight = static_cast<int>(afxGlobalData.GetRibbonImageScale() * 60.0);
	const int bottomAreaSpacing = static_cast<int>(afxGlobalData.GetRibbonImageScale() * 10.0);
	const int vectorEditBoxHeight = static_cast<int>(afxGlobalData.GetRibbonImageScale() * 32.0);
	m_wndClassView.SetWindowPos(nullptr, rectClient.left + 1, rectClient.top + cyTlb + searchBarHeight + 1, rectClient.Width() - 2, rectClient.Height() - cyTlb - searchBarHeight - bottomAreaHeight - 2, SWP_NOACTIVATE | SWP_NOZORDER);

	m_loc.MoveWindow(rectClient.left + 1, rectClient.bottom - bottomAreaHeight + bottomAreaSpacing, rectClient.Width() - 2, vectorEditBoxHeight);
	m_loc.Redraw();

	Invalidate();
}

